    inline const QHash<QString, int>& COMMON_WORDS()
    {
        std::vector<QString> words;
        addManualFrequentWords(words);
        words.push_back(NUMBER_WITH_LESS_THAN_2_LETTERS);
        addCalculatedCommonWords(words);
        addAbbreviations(words);

        static QHash<QString, int> dictionary = [&]()
        {
            QHash<QString, int> dict;
            dict.reserve(words.size() * 2.0L);

            int i = 0;
            for (const QString& originalString : words)
            {
                dict.insert(originalString, i);
                QString transformedString = OsmAnd::SearchAlgorithms::replaceGermanSS(originalString);
                transformedString = OsmAnd::ICU::stripDiacritics(transformedString);
                if (originalString != transformedString)
                {
                    dict.insert(transformedString, i);
                }
                ++i;
            }
            return dict;
        }();
        return dictionary;
    }

    inline QList<QString>& REGION_NAMES()
    {
        static QList<QString> regionNames;
        return regionNames;
    }
    
    union CommonWords
    {
        static inline int getFrequentlyUsed(const QString& name)
        {
            int rs = REGION_NAMES().size();
            auto it = FREQUENTLY_USED_WORDS().find(name);
            if (it != FREQUENTLY_USED_WORDS().end())
            {
                return *it + rs;
            }
            return -1;
        }

        static inline int getRegionIndex(const QString& name)
        {
            return REGION_NAMES().indexOf(name);
        }

        static inline int getCommonSearch(QString name)
        {
            bool startsWithDigit = false;
            for (int i = 0; i < name.length(); i++)
            {
                if (name.at(i).isDigit())
                {
                    startsWithDigit = true;
                    break;
                }
                else if (name.at(i).isLetter())
                {
                    break;
                }
            }
            if (startsWithDigit && letters(name) < 2)
            {
                name = NUMBER_WITH_LESS_THAN_2_LETTERS;
            }
            
            auto it = COMMON_WORDS().find(name);
            // higher means better for search
            if (it != COMMON_WORDS().end())
            {
                return *it;
            }
            int ri = getRegionIndex(name);
            if (ri != -1)
            {
                return COMMON_WORDS().size() + ri + 1;
            }
            int fq = getFrequentlyUsed(name);
            if (fq != -1)
            {
                return COMMON_WORDS().size() + fq + 1;
            }
            return -1;
        }
        
        static int letters(const QString &s)
        {
                int count = 0;
                for (int i = 0; i < s.length(); i++)
                {
                    if (!s[i].isDigit() && s[i].isLetter())
                    {
                        count++;
                    }
                }
                return count;
        }

        static inline bool isNumber2Letters(const QString& name)
        {
            return !name.isEmpty() && name.front().isDigit() && letters(name) < 2;
        }

        static inline int getCommonGeocoding(const QString name)
        {
            auto it = COMMON_WORDS().find(name);
            if (it != COMMON_WORDS().end())
            {
                return *it;
            }
            if (name.length() > 2 && REGION_NAMES().contains(name))
            {
                return COMMON_WORDS().size();
            }
            return -1;
        }
        
        static inline void addAllRegions(const QStringList& rawRegionNames)
        {
            auto& regions = REGION_NAMES();
            QSet<QString> seen;
            seen.reserve(rawRegionNames.size() * 1.5);
            regions.reserve(rawRegionNames.size() * 1.5);

            for (const QString& rawName : rawRegionNames)
            {
                QString original = rawName.toLower();
                if (!seen.contains(original))
                {
                    seen.insert(original);
                    regions.push_back(original);
                }
                QString transformed = OsmAnd::SearchAlgorithms::replaceGermanSS(original);
                transformed = OsmAnd::ICU::stripDiacritics(transformed);
                if (transformed != original && !seen.contains(transformed))
                {
                    seen.insert(transformed);
                    regions.push_back(transformed);
                }
            }
        }
    };
